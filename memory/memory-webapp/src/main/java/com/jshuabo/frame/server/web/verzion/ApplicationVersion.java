package com.jshuabo.frame.server.web.verzion;

/**
 * @ClassName: ApplicationVersion
 * @Description: 系统版本号
 * @author: lianghe.yuan
 * @date: 2014年5月23日 上午10:31:54
 */
public class ApplicationVersion {
    private String title;
    private String name;
    private String version;
    private String extendProp1;
    private String extendProp2;
    
    public ApplicationVersion() {
    }
    
    public ApplicationVersion(String title, String name, String version, String extendProp1, String extendProp2) {
        this.title = title;
        this.name = name;
        this.version = version;
        this.extendProp1 = extendProp1;
        this.extendProp2 = extendProp2;
    }
    
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getExtendProp1() {
        return extendProp1;
    }

    public void setExtendProp1(String extendProp1) {
        this.extendProp1 = extendProp1;
    }

    public String getExtendProp2() {
        return extendProp2;
    }

    public void setExtendProp2(String extendProp2) {
        this.extendProp2 = extendProp2;
    }
}
